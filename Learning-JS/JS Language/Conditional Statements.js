var a = 10;
var b = 20;
var key = "2";

// If Else Conditional Statements
if(a>b){
    console.log("A is greater");
}else{
    console.log("B is greater");
}

// While Loop
while (true) {
    console.log("Playing");
    if (a>b) {
        break;
    }
    a++;
}

// Switch Statements
switch (key) {
    case 1:
        console.log("first");
        break;
    case 2:
        console.log("second");
        break
    default:
        console.log("Other")
        break;
}
//  For Loop
for(let i = 1; i<=10; i++){
    console.log(i);
}

