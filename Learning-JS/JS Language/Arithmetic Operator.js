var a = 20;
var b = 30;
var c = "100";

// Arithmetic Operator

// Remainder
console.log(a%2);

// Increment
console.log(a++);
console.log(++a);

// Decrement
console.log(b++);
console.log(++b);

// Unary Negation
console.log(-a);

// Unary Plus
console.log(c + 2);
console.log(+c + 2);

// Exponential Operator
console.log(a**2);