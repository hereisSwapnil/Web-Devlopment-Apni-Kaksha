var a = 10;
var b = 20;

// Assignment Operator
var c = a;

// Addition
a = c+b;

// Substraction
a -= b;

// Multiplication
a *= a;

// Division
a /= 2;

// Remainder
a %= 3;

// Exponential
a **= 10;

// Logical AND
// stores value of b in a only when a is true
a = 1;
a &&= b;

// Logical OR
// stores value of c in a only when a is false
a = 0;
a ||= c;
